int bfs(int ind) {
	int allin[350];//节点状态
	int con[350];//步长
	memset(con, 0, sizeof(con));
	memset(allin, 0, sizeof(allin));
	int times = 0, ans = 0, next = 0;
	if (ind == 0) { return con[150]; }
	allin[150] = 1;
	queue<int>sor;
	sor.push(times);//压入起点
	while (!sor.empty()) {
		ans = sor.front();
		if (ans == ind) { return con[150 + ans]; }
		sor.pop();//压出无关节点
		for (int i = 0; i < 6; i++) {
			next = ans + steps[i];
			if (allin[150+next]==0) {
				times = 150 + next;
				allin[times] = 1;
				con[times] = con[150 + ans] + 1;
				sor.push(next);//压入新节点
			}
		}
		
	}
}
